    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtCodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(
                this,
                "Se requiere el codigo del usuario",
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        } else {
            String codigo = txtCodigo.getText();
            Usuario objUsu = UsuarioDAO.buscar(codigo);

            if (objUsu != null) {
                txtCodigo.setText(objUsu.getId());
                txtNombres.setText(objUsu.getNombres());
                txtDni.setText(objUsu.getDni());

                if (objUsu instanceof Estudiante) {
                    comboTipoUsr.setSelectedItem("Estudiante");
                    lblDatoExtra.setText("Carrera ");
                    txtDatoExtra.setText(((Estudiante) objUsu).getCarrera());
                } else if (objUsu instanceof Docente) {
                    comboTipoUsr.setSelectedItem("Docente");
                    lblDatoExtra.setText("Facultad ");
                    txtDatoExtra.setText(((Docente) objUsu).getFacultad());
                } else if (objUsu instanceof Administrativo) {
                    comboTipoUsr.setSelectedItem("Administrativo");
                    lblDatoExtra.setText("Area ");
                    txtDatoExtra.setText(((Administrativo) objUsu).getArea());
                }
            } else {
                JOptionPane.showMessageDialog(
                    this,
                    "Usuario no encontrado",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
                );
                limpiar();
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!txtCodigo.getText().isEmpty()) {
            String id = txtCodigo.getText();
            String cadena = "¿Estas seguro de eliminar " + txtNombres.getText() + "?";
            int respuesta = Funciones.dialogoPregunta(cadena);

            if (usuarioDAO.eliminar(id)) {
                if (respuesta == JOptionPane.YES_OPTION) {
                    JOptionPane.showMessageDialog(
                        this,
                        "Usuario eliminado",
                        "Mensaje",
                        JOptionPane.INFORMATION_MESSAGE
                    );
                    listado();
                    limpiar();
                } else {
                    JOptionPane.showMessageDialog(
                    this,
                    "Usuario no encontrado",
                    "Error",
                    JOptionPane.ERROR_MESSAGE
                );
                }
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

