    private void listado() {
        DefaultTableModel modelo = (DefaultTableModel) tablaUsuarios.getModel();
        modelo.setRowCount(0);

        Usuario[] datos = UsuarioDAO.obtener();
        int cantidad = UsuarioDAO.getCantidad();

        for (int i = 0; i < cantidad; i++) {
            Usuario objUsu = datos[i];
            String datoExtra = "";

            if (objUsu instanceof Estudiante estudiante) {
                datoExtra = estudiante.getCarrera();
            } else if (objUsu instanceof Docente docente) {
                datoExtra = docente.getFacultad();
            } else if (objUsu instanceof Administrativo administrativo) {
                datoExtra = administrativo.getArea();
            }

            if (objUsu != null) {
                modelo.addRow(new Object[]{
                    objUsu.getId(),
                    objUsu.getNombres(),
                    objUsu.getDni(),
                    objUsu.getRol(),
                    datoExtra
                });
            }
        }
    }

    private void limpiar() {
        txtCodigo.setText("");
        txtNombres.setText("");
        txtDni.setText("");
        txtDatoExtra.setText("");
        comboTipoUsr.setSelectedIndex(0);
        lblDatoExtra.setText("");
        txtCodigo.requestFocus();
    }

